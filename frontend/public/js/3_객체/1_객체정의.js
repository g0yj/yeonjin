 // 1. 객체선언
 let 객체1={아이디: 'qwe' , 비밀번호:'1234' , 이름:'유재석'}

	// vs let 배열1=['qwe' , '1234' , '유재석']
	
//2. 객체호출
console.log(객체1)  // 객체1의 모든 정보 호출
console.log('아이디 속성 호출: '+객체1.아이디)//객체 1의 특정(아이디) 속성 내 데이터 호출
