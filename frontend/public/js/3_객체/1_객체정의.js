	// console: 내장객체 -> console객체에서 log()함수를 호출
	// document: 내장객체-> document객체에서 querySelector()함수 호출

//3. 객체의 속성 내 데이터 변경
객체1.비밀번호='123456'   //[변경]객체내 비밀번호 속성 호출해서 새로운 데이터 대입


//4. 객체 내 속성 추가
객체1.주소='안산'   // 객체내 주소 속성이 없으므로 호출이 아닌 추가돼서 값이 대입

//5. 객체내 속성 삭제
delete 객체1.주소;

